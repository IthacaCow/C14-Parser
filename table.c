int CodeTableSize = sizeof( codeTable ) / sizeof( CodeTable );

Code fetchCode( char* name ){
	for (int i = 0; i < CodeTableSize; ++i)
		if( strcmp( name , codeTable[i].name ) == 0 )
			return codeTable[i].code;
	return 0;
}
